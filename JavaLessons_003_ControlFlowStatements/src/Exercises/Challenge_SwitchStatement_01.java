package Exercises;

public class Challenge_SwitchStatement_01 {

    public static void main(String[] args) {

        char alphabet = 'E';

        switch (alphabet) {

            case 'A' :
                System.out.println("Letter is a");
                break;
            case 'B' :
                System.out.println("Letter is b");
                break;
            case 'C' :
                System.out.println("Letter is c");
                break;
            case 'D' :
                System.out.println("Letter is d");
                break;
            case 'E' :
                System.out.println("Letter is e");
                break;
            default:
                System.out.println("no letter");

        }



    }

}
